class Pokemon {
  constructor(id, name, abilities, images, experience, height, weight, types, color, habitat ) {
    this.id = id
    this.name = name
    this.abilities = abilities
    this.images = images
    this.experience = experience
    this.height = height
    this.weight = weight
    this.types = types
    this.color = color
    this.habitat = habitat
  }
}

export default Pokemon